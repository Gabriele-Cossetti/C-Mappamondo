void DrawGlobo (HDC hdc, double Y[], double A, double B, double R, double PI, int Dimensione, int cxClient, int cyClient)
{
     double F, T, XX, YY, GG;
     bool W = false;
     int I,K1,K2;
     GG = 0;
		 
		       SetROP2 (hdc, R2_NOTMERGEPEN);
               Ellipse (hdc, cxClient/2-R*(Dimensione/2), (cyClient-50)/2-R*(Dimensione/2), cxClient/2+R*(Dimensione/2), (cyClient-45)/2+R*(Dimensione/2));
               SetROP2 (hdc, R2_COPYPEN);

			   for (I=0; I<5438; I+=2){
				   F=Y[I];
				   T=Y[I+1];

				   if (F==500 && T==500){
					   W=false;
					   I+=2;
					   F=Y[I];
				       T=Y[I+1];
				   }

				   // trasformazione coordinate da polari tridimensionali a cartesiane bidimensionali
				   F = F * PI / 180; 
				   T = T * PI / 180;
				   XX = R * cos(T) * sin(F + B);
                   YY = R * sin(T) * cos(A) - R * cos(T) * cos(F + B) * sin(A);
                   K1 = (long)Dimensione / 2 * XX + cxClient / 2;
                   K2 = (long)Dimensione / 2 * YY + cyClient / 2;
                   K2 = (cyClient - 25) - K2;

				   // mappatura parte nascosta del globo
                   GG = cos(T) * cos(F + B) + sin(T) * tan(A);
                   if (cos(A) < 0) GG=-GG;
                   if (GG < -0.0005){
					   W=false;
				   }

                   if (W == true){
                       LineTo (hdc, K1, K2);
				   }
				   
                   if (W == false){
					   MoveToEx (hdc, K1, K2, NULL);
					   W = true; 
				   }
			   }
}


